def debugprint(*args):
    if debug == True:
        print(*args)


def is_valid(springs, counts):
    grouped_items = [list(g) for _, g in itertools.groupby(springs)]
    spring_counts = [len(x) for x in grouped_items if x[0] == "#"]
    return spring_counts == counts


def solve_possibilities(springs, counts):
    total_springs_expected = sum(counts)

    springs_so_far = len([x for x in springs if x == "#"])
    question_marks = len([x for x in springs if x == "?"])
    springs_we_need = total_springs_expected - springs_so_far

    total_items = len(springs)

    total = 0

    iterations = pow(2, question_marks)
    for i in range(iterations):
        # count the number of 1s in the binary representation
        binary = bin(i)[2:]
        # pad the binary representation with 0s so there are the same number of digits as there are question marks
        binary = binary.zfill(question_marks)
        number_of_ones = binary.count("1")
        if number_of_ones != springs_we_need:
            debugprint(
                f"Skipping {i} = {binary} because it has {number_of_ones} ones and there are {springs_we_need} springs that we need"
            )
            continue
        debugprint(
            f"{i} = {binary} is being checked because it has {number_of_ones} ones and there are {springs_we_need} springs that we need"
        )
        springs_copy = springs
        for j in range(question_marks):
            is_spring = binary[j] == "1"
            springs_copy = springs_copy.replace("?", "#" if is_spring else ".", 1)
        debugprint(springs_copy, " is being check for validity against", counts)
        if is_valid(springs_copy, counts):
            debugprint("Valid!")
            total += 1

    return total

    debugprint(total_springs_expected, "=total_springs_expected")
    debugprint(springs_so_far, "=springs_so_far")

    if is_valid(springs, counts):
        debugprint("Valid!")
        return 1

    possibilities = 0
    for spring in springs:
        if spring == "?":
            possibilities += 1
    return possibilities


def parse_line(line):
    line_regex = r"^([\.\?#]+)\s(.*)$"
    matches = re.match(line_regex, line)
    if not matches:
        raise Exception("Invalid line: " + line)
    springs = matches.group(1)
    counts_raw = matches.group(2)
    counts = [int(x) for x in counts_raw.split(",") if x != ""]
    debugprint(springs, counts)
    return solve_possibilities(springs, counts)
    return 0


def parse_input(input):
    total = 0
    lines = input.splitlines()
    for line in lines:
        total += parse_line(line)

    return total


